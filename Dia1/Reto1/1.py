
#- Desarrolla un programa que calcule y imprima todos los numeros pares entre 10 y 99.


n_limite = int(input("Ingresa el valor de n; "))
pares = []
for i in range(0, n_limite + 1):
    if i % 2 == 0:
        pares.append(i)
print(f"Numeros pares del arreglo {pares} ")

